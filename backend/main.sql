CREATE DATABASE IF NOT EXISTS travel;

USE travel;


CREATE TABLE users (
    user_id CHAR(36) PRIMARY KEY,   -- UUID
    username VARCHAR(50) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    pfp VARCHAR(255) DEFAULT NULL,   -- profile
    age INT,
    budget INT,
    beach TINYINT(1) DEFAULT 0,
    trekking TINYINT(1) DEFAULT 0,
    culture TINYINT(1) DEFAULT 0,
    adventure TINYINT(1) DEFAULT 0,
    travel_month INT CHECK (travel_month BETWEEN 1 AND 12),
    destination VARCHAR(100) DEFAULT NULL,
    latitude DECIMAL(9,6) NOT NULL,
    longitude DECIMAL(9,6) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_users_lat_lon ON users(latitude, longitude);


CREATE TABLE poi (
    poi_id INT PRIMARY KEY AUTO_INCREMENT,
    poi_name VARCHAR(100) NOT NULL,
    latitude DECIMAL(9,6) NOT NULL,
    longitude DECIMAL(9,6) NOT NULL,
    category VARCHAR(50) NOT NULL
);


CREATE INDEX idx_poi_lat_lon ON poi(latitude, longitude);


CREATE TABLE ratings (
    user_id CHAR(36) NOT NULL,
    poi_id INT NOT NULL,
    rating_food INT NOT NULL CHECK (rating_food BETWEEN 1 AND 5),
    rating_safety INT NOT NULL CHECK (rating_safety BETWEEN 1 AND 5),
    rating_fun INT NOT NULL CHECK (rating_fun BETWEEN 1 AND 5),
    PRIMARY KEY (user_id, poi_id),
    FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE,  -- data integrity
    FOREIGN KEY (poi_id) REFERENCES poi(poi_id) ON DELETE CASCADE
);


CREATE INDEX idx_ratings_user ON ratings(user_id);
CREATE INDEX idx_ratings_poi ON ratings(poi_id);



CREATE TABLE token_blacklist (
    jti VARCHAR(64) PRIMARY KEY,
    expires_at DATETIME NOT NULL
);


CREATE TABLE travel_groups (
    group_id CHAR(36) PRIMARY KEY,
    group_name VARCHAR(100) NOT NULL,
    description TEXT,
    destination VARCHAR(100) DEFAULT NULL,
    latitude DECIMAL(9,6) NOT NULL,
    longitude DECIMAL(9,6) NOT NULL,
    travel_month INT CHECK (travel_month BETWEEN 1 AND 12),
    max_members INT DEFAULT 4 CHECK (max_members BETWEEN 2 AND 10),
    created_by CHAR(36) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (created_by) REFERENCES users(user_id) ON DELETE CASCADE
);

CREATE UNIQUE INDEX idx_group_name ON travel_groups(group_name);

CREATE TABLE group_join_requests (
    request_id CHAR(36) PRIMARY KEY,
    group_id CHAR(36) NOT NULL,
    user_id CHAR(36) NOT NULL,
    status ENUM('pending','approved','rejected') DEFAULT 'pending',
    requested_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(group_id, user_id),
    FOREIGN KEY (group_id) REFERENCES travel_groups(group_id) ON DELETE CASCADE,
    FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE
);

CREATE INDEX idx_group_requests_user ON group_join_requests(user_id);
CREATE INDEX idx_group_requests_group ON group_join_requests(group_id);


CREATE TABLE group_members (
    group_id CHAR(36) NOT NULL,
    user_id CHAR(36) NOT NULL,
    role ENUM('admin','member') DEFAULT 'member',
    joined_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (group_id, user_id),
    FOREIGN KEY (group_id) REFERENCES travel_groups(group_id) ON DELETE CASCADE,
    FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE
);

CREATE INDEX idx_group_members_user ON group_members(user_id);
CREATE INDEX idx_group_members_group ON group_members(group_id);


CREATE TABLE private_messages (
    message_id CHAR(36) PRIMARY KEY,
    sender_id CHAR(36) NOT NULL,
    receiver_id CHAR(36) NOT NULL,
    message VARCHAR(2000) NOT NULL,
    sent_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_read BOOLEAN DEFAULT FALSE,
    FOREIGN KEY (sender_id) REFERENCES users(user_id) ON DELETE CASCADE,
    FOREIGN KEY (receiver_id) REFERENCES users(user_id) ON DELETE CASCADE
);

CREATE INDEX idx_private_pair ON private_messages(sender_id, receiver_id, sent_at);
CREATE INDEX idx_private_receiver ON private_messages(receiver_id, sender_id, sent_at);


CREATE TABLE group_messages (
    message_id CHAR(36) PRIMARY KEY,
    group_id CHAR(36) NOT NULL ,
    sender_id CHAR(36) NOT NULL,
    message VARCHAR(2000) NOT NULL,
    sent_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (group_id) REFERENCES travel_groups(group_id) ON DELETE CASCADE,
    FOREIGN KEY (sender_id) REFERENCES users(user_id)
);

CREATE INDEX idx_group_chat ON group_messages(group_id, sent_at);


