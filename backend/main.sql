CREATE DATABASE IF NOT EXISTS travel;

USE travel;


CREATE TABLE destination (
    destination_id INT PRIMARY KEY AUTO_INCREMENT,
    destination_name VARCHAR(100),
    latitude DECIMAL(9,6),
    longitude DECIMAL(9,6)
);


